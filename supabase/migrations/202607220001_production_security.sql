create or replace function public.protect_hoof_visit_audit_fields()
returns trigger
language plpgsql
set search_path = public
as $$
declare
  session_employee_id uuid;
  session_employee_name text;
begin
  if tg_op = 'INSERT' then
    session_employee_id := public.hoof_session_employee_id();
    select name into session_employee_name
    from public.employees where id = session_employee_id;
    if session_employee_id is null or session_employee_name is null then
      raise exception 'Sessão de funcionário inválida';
    end if;
    new.employee_id := session_employee_id;
    new.employee_name := session_employee_name;
    new.device_id := public.hoof_session_device_id();
    return new;
  end if;

  if new.farm_id is distinct from old.farm_id
     or new.created_at is distinct from old.created_at
     or new.date is distinct from old.date
     or new.employee_id is distinct from old.employee_id
     or new.employee_name is distinct from old.employee_name
     or new.device_id is distinct from old.device_id then
    raise exception 'Campos de auditoria da visita são imutáveis';
  end if;
  new.updated_at := now();
  return new;
end;
$$;

drop trigger if exists protect_hoof_visit_audit_fields on public.hoof_visits;
