create or replace function public.activate_hoof_device(
  p_farm_id uuid,
  p_device_name text default 'Navegador'
)
returns jsonb
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  selected_session public.employee_sessions%rowtype;
  selected_farm public.farms%rowtype;
  selected_client public.clients%rowtype;
  selected_license public.licenses%rowtype;
  existing_device public.devices%rowtype;
  active_farm_devices integer;
  active_client_devices integer;
begin
  select * into selected_session from public.hoof_current_session() limit 1;
  if selected_session.id is null then
    return jsonb_build_object('ok', false, 'message', 'Sessão expirada. Entre novamente.');
  end if;

  select * into selected_farm
  from public.farms
  where id = p_farm_id
    and client_id = selected_session.client_id
    and status = 'active';
  if selected_farm.id is null or not exists (
    select 1 from public.employee_farms assignment
    where assignment.employee_id = selected_session.employee_id
      and assignment.farm_id = p_farm_id
  ) then
    return jsonb_build_object('ok', false, 'message', 'Funcionário sem acesso a esta fazenda.');
  end if;

  select * into selected_client
  from public.clients
  where id = selected_session.client_id
    and status = 'active';
  if selected_client.id is null then
    return jsonb_build_object('ok', false, 'message', 'Empresa bloqueada ou inativa.');
  end if;

  select * into selected_license
  from public.licenses
  where farm_id = p_farm_id
    and status = 'active'
    and (starts_at is null or starts_at <= now())
    and (expires_at is null or expires_at >= now())
  order by expires_at desc nulls first
  limit 1;
  if selected_license.id is null then
    return jsonb_build_object('ok', false, 'message', 'Licença expirada ou bloqueada.');
  end if;

  select * into existing_device
  from public.devices
  where farm_id = p_farm_id
    and device_id = selected_session.device_id
  limit 1;
  if existing_device.id is not null and existing_device.status = 'blocked' then
    return jsonb_build_object('ok', false, 'message', 'Este aparelho foi bloqueado.');
  end if;

  if existing_device.id is null then
    select count(*) into active_farm_devices
    from public.devices
    where farm_id = p_farm_id and status = 'active';
    if selected_farm.max_devices is not null
       and active_farm_devices >= selected_farm.max_devices then
      return jsonb_build_object('ok', false, 'message', 'Limite de aparelhos desta fazenda atingido.');
    end if;

    select count(*) into active_client_devices
    from public.devices device
    join public.farms farm on farm.id = device.farm_id
    where farm.client_id = selected_session.client_id
      and device.status = 'active';
    if selected_client.max_devices is not null
       and active_client_devices >= selected_client.max_devices then
      return jsonb_build_object('ok', false, 'message', 'Limite de aparelhos da empresa atingido.');
    end if;
  end if;

  insert into public.devices (
    farm_id, employee_id, device_id, device_name, status, last_seen_at
  ) values (
    p_farm_id,
    selected_session.employee_id,
    selected_session.device_id,
    left(coalesce(nullif(trim(p_device_name), ''), 'Navegador'), 120),
    'active',
    now()
  )
  on conflict (farm_id, device_id) do update
  set employee_id = excluded.employee_id,
      device_name = excluded.device_name,
      last_seen_at = now();

  return jsonb_build_object(
    'ok', true,
    'license_starts_at', selected_license.starts_at,
    'license_expires_at', selected_license.expires_at
  );
end;
$$;

create or replace function public.validate_hoof_access(p_farm_id uuid)
returns jsonb
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  selected_session public.employee_sessions%rowtype;
  selected_employee public.employees%rowtype;
  selected_farm public.farms%rowtype;
  selected_device public.devices%rowtype;
  selected_license public.licenses%rowtype;
begin
  select * into selected_session from public.hoof_current_session() limit 1;
  if selected_session.id is null then
    return jsonb_build_object('ok', false, 'message', 'Sessão expirada. Entre novamente.');
  end if;

  select * into selected_employee
  from public.employees
  where id = selected_session.employee_id
    and client_id = selected_session.client_id
    and status = 'active';
  if selected_employee.id is null then
