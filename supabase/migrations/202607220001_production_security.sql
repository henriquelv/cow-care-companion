create or replace function public.hoof_current_session()
returns setof public.employee_sessions
language sql
stable
security definer
set search_path = public, extensions
as $$
  select session.*
  from public.employee_sessions session
  where session.token_hash = encode(
      digest(coalesce(public.hoof_request_header('x-hoof-session'), ''), 'sha256'),
      'hex'
    )
    and session.device_id = coalesce(public.hoof_request_header('x-hoof-device-id'), '')
    and session.revoked_at is null
    and session.expires_at > now()
  order by session.created_at desc
  limit 1;
$$;

